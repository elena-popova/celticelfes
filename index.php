<h5><a name="dr">Devon Rex</a></h5>
 <p>A bit about Devon Rex breed:<br/>
 The Devon Rex is a breed of cat with a curly, very soft short coat similar to that of the Cornish Rex. They are often associated with being one of the most hypo-allergenic cats available because of their type of coat.

The first Devon was discovered by Beryl Cox in Buckfastleigh, Devon, UK in 1960.<br/>
<img src="images/eclipse.jpg"alt="tom" width="400" height="400" align="right" style="margin-left:20px" />
 Devons, which are medium sized cats, are often called "pixie cats" or "alien cats" because of their unique appearance. Their uncommonly large ears are set low on the sides of their wide heads, their eyes are large, and their noses are slightly upturned. Unlike most cats, their whiskers are very short and often curled to such an extent that it may appear as if they have no whiskers. Their body type is distinctly lightly built. Their long, sturdy legs are well suited for long leaps, and their toes are unusually large. Devon Rex cats come in all colors. The ears are large and slightly rounded.<br/>
The typical Devon is active, mischievous, playful, and very people-oriented. They have been described as a cross between a cat, a dog and a monkey (or, more famously, as "a monkey in a cat suit").They are a very intelligent breed; the typical Devon Rex can be trained to walk on a leash, fetch or perform all manner of tricks usually associated with canines, like jump, heel and tag to name a few.
The Devon Rex is a faithful companion and will usually follow the object of their affection from room to room, waiting for the opportunity to leap onto their arms, lap, or shoulder. </p>
